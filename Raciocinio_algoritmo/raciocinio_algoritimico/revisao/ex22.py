# 22. Explique o que é um dicionário e como acessar seus valores.

# R. Um dicionário é um conjunto de valores armazenados em uma unica variavel e que podem ser acessados através de chaves atribuidas
# Podemos acessar um valor da seguinte maneira: dicionário['chave']

dicionario = {'chave' : 'valor'}

print(dicionario['chave'])
